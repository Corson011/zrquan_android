public class ThemeSearch {
    private String name;
    private int substanceId;
    private String substanceType;
    private String description;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getSubstanceId() {
        return substanceId;
    }

    public void setSubstanceId(int substanceId) {
        this.substanceId = substanceId;
    }

    public String getSubstanceType() {
        return substanceType;
    }

    public void setSubstanceType(String substanceType) {
        this.substanceType = substanceType;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

}
